package basics_of_java.Data_typess;

public class Data_Tpes {
    //Java programs to declare, initialize, modify all the primitive data types.
    public static void main(String[] args) {
        // boolean
        boolean boo = true;
        System.out.println(boo);    // prints true

        byte ranges =12;

        System.out.println(ranges);    // prints 12

        int range = -1250000;
        System.out.println(range);  // print -1250000

        long rang = -12332200000L;
        System.out.println(range);    // prints -12332200000

        double ran = -12.3;
        System.out.println(ran);  // prints -12.3

        float num = -12.3f;
        System.out.println(num);  // prints -12.3

        char letter = 'Q';
        System.out.println(letter);  // prints Q
    }
}
